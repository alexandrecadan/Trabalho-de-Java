    private void btnExcluirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirClienteActionPerformed
        try {
            ClienteDAO dao = new ClienteDAO();
            int[] linhasSelecionadas = tabelaCliente.getSelectedRows();
            List<Cliente> listaExcluir = new ArrayList();
            for (int i = 0; i < linhasSelecionadas.length; i++) {
                Cliente cliente = clienteTableModel.getCliente(linhasSelecionadas[i]);
                dao.remover(cliente);
                listaExcluir.add(cliente);
            }
            for(Cliente cliente:listaExcluir){
                clienteTableModel.removeCliente(cliente);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Erro ao realizar exclus√£o de cliente(s).", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirClienteActionPerformed

    private void btnInserirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirClienteActionPerformed
        String nome = edtNomeCliente.getText();
        String sobrenome = edtSobrenomeCliente.getText();
        String telefone = edtTelefoneCliente.getText();
       
        Cliente cliente = new Cliente(nome, sobrenome, telefone);
        try {
            ClienteDAO dao = new ClienteDAO();
            dao.cadastrar(cliente);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Erro ao inserir cliente no banco de dados. E="+ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        clienteTableModel.adicionaCliente(cliente);
    }//GEN-LAST:event_btnInserirClienteActionPerformed

    private void btnAtualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarClienteActionPerformed
        linhaClicada = tabelaCliente.getSelectedRow();
        if(linhaClicada!=-1){
            Cliente cliente = clienteTableModel.getCliente(linhaClicada);
            int cod = (int) clienteTableModel.getValueAt(linhaClicada, 0);
            cliente.setNome(edtNomeCliente.getText());
            cliente.setSobrenome(edtSobrenomeCliente.getText());
            cliente.setTelefone(edtTelefoneCliente.getText());
            cliente.setId(cod);
            ClienteDAO dao = null;
            try {
                dao = new ClienteDAO();
                dao.alterar(cliente);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Erro ao atualizar cliente no banco de dados. E="+ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
            clienteTableModel.fireTableRowsUpdated(linhaClicada, linhaClicada);
        }
    }//GEN-LAST:event_btnAtualizarClienteActionPerformed

